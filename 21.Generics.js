function printArray(arr) {
  for (let i = 0; i < arr.length; i++) {
    console.log(arr[i]);
  }
}

// Example usage:
const numberArray = [1, 2, 3, 4, 5];
const stringArray = ["apple", "banana", "cherry"];

printArray(numberArray); // Prints the numbers in the array
printArray(stringArray); // Prints the strings in the array
